//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
import { FunctionComponent, Fragment, ReactElement } from 'react';
import { FaRegPaperPlane } from 'react-icons/fa';
import { twMerge } from 'tailwind-merge';
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export type FormFieldOptions = {
  type: string;
  placeholder: string;
  required: boolean;
  minLength?: number;
  pattern?: string;
  title?: string;
  autoComplete?: string;
};

type ContactFormProps = {
  formFieldOpts: Array<FormFieldOptions>;
  formCallback: (field: FormFieldOptions, index: number) => ReactElement;
};
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export const ContactForm: FunctionComponent<ContactFormProps> = ({
  formFieldOpts,
  formCallback,
}) => {
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  return (
    <Fragment>
      {/*  ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞  */}
      <form className='mt-8 cursor-pointer space-y-4'>
        {/* Input fields MAPPED ∞∞∞ */}
        {formFieldOpts.map(formCallback)}
        {/* TEXTAREA MESSAGE FIELD ∞∞∞ */}
        <textarea
          placeholder='Message'
          required
          minLength={10}
          rows={6}
          className='w-full rounded-lg px-4 pt-3 text-sm text-gray-800'
        />
        {/* SEND MESSAGE BUTTON ∞∞∞ */}
        <button
          type='submit'
          className={twMerge(
            clsx(
              '!mt-6 flex w-full items-center justify-center rounded-lg',
              'bg-thumbsup-dark px-4 py-3 text-sm tracking-wide',
              'text-white hover:bg-reggie-orange',
            ),
          )}
        >
          {/* PLANE FOR MAIL ICON ∞∞∞ */}
          <FaRegPaperPlane className='mr-2 w-6 font-semibold' />
          Send Message
        </button>
      </form>
      {/*  ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞  */}
    </Fragment>
  );
};
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
