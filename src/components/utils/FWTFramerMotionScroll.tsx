import { FunctionComponent, Fragment, ReactNode } from 'react';
import { MotionProps, motion } from 'framer-motion';
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

type FWTViewportAnimationProps = MotionProps & {
  /** Section content */
  children: ReactNode;
  /** Optional styles */
  className?: string;
  /** Disable the default fade animation */
  disableFade?: boolean;
};
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export const FWTFramerMotionScroll: FunctionComponent<FWTViewportAnimationProps> = ({
  children,
  className,
  disableFade = false,
  ...motionProps
}) => {
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  // Animation variants
  const fadeVariants = {
    visible: { opacity: 1, x: 0, scale: 1, transition: { duration: 0.65, ease: 'easeOut' } },
    hidden: {
      opacity: 0,
      x: -20,
      scale: 0.65,
      transition: { duration: 0.5, ease: 'easeOut' },
    },
  };
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  return (
    <Fragment>
      {/*  ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞  */}
      <motion.section
        className={`transition-none ${className}`}
        variants={!disableFade ? fadeVariants : undefined}
        initial='hidden'
        whileInView='visible'
        viewport={{
          once: false,
          amount: 0.3,
          margin: '0px 0px -10% 0px',
        }} // Mobile optimization
        {...motionProps}
      >
        {children}
      </motion.section>
      {/*  ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞  */}
    </Fragment>
  );
};
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
