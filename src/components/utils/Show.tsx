import { ReactNode } from 'react';
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

/**
 * A conditional rendering component that displays content based on a condition.
 *
 * @example
 * // Basic usage
 * <Show
 *   condition={isLoading}
 *   then={<Content />}
 *   otherwise={<Spinner />}
 * />
 *
 * // With function expression
 * <Show
 *   condition={user}
 *   then={(user) => <Welcome name={user.name} />}
 *   otherwise={<LoginPrompt />}
 * />
 */
type ShowProps<TShowType> = {
  /** The condition that determines which content to display */
  condition: TShowType | undefined | null | false;
  /** Content to display when condition is true */
  then: ReactNode | ((item: TShowType) => ReactNode);
  /** Optional content to display when condition is false */
  otherwise?: ReactNode;
};
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

// TODO: Remove when used in the app
// noinspection JSUnusedGlobalSymbols
export function Show<TProps>({ condition, otherwise, then }: ShowProps<TProps>): ReactNode {
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  if (condition) {
    return typeof then === 'function' ? then(condition) : then;
  }
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  return otherwise || null;
}

