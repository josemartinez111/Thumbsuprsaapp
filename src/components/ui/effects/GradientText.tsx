import { Fragment, FunctionComponent } from 'react';
import { twMerge } from 'tailwind-merge';
import clsx from 'clsx';
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

type GradientTextProps = {
  text: string;
  className?: string;
};
//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞

export const GradientText: FunctionComponent<GradientTextProps> = ({
  text,
  className = '',
}) => {
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  // Assuming this is inside your GradientText component
  const baseTextStyles = clsx(
    'whitespace-nowrap',
    'bg-gradient-to-r from-blue-500 via-teal-500 to-pink-500',
    'bg-clip-text text-transparent',
    'text-xl tablet:text-3xl laptop:text-4xl',
    'font-extrabold',
  );

  const gradientTextClasses = twMerge(baseTextStyles, className);
  // ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
  return (
    <Fragment>
      <div className='flex items-center justify-center'>
        <div className='relative flex items-center'>
          <span
            className={twMerge('absolute inset-0 select-none blur-xl', gradientTextClasses)}
          >
            {text}
          </span>
          <h1 className={twMerge('relative', gradientTextClasses)}>{text}</h1>
        </div>
      </div>
    </Fragment>
  );
};

//⚫️ ∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞∞
